<p>The Max Planck Institute for Astrophysics (MPA) is a research institute located in Garching, just north of Munich, Bavaria, Germany. It is one of many scientific research institutes belonging to the Max Planck Society.</p>

<p>The MPA is widely considered to be one of the leading institutions in the world for theoretical astrophysics research. According to Thomson Reuters, from 1999-2009 the Max Planck Society as a whole published more papers and accumulated more citations in the fields of physics and space science than any other research organization in the world.</p>
