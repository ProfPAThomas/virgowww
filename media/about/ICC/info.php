<p>The Institute for Computational Cosmology (ICC) is a leading international
centre for research into the origin and evolution of the Universe.</p>

<p>ICC researchers address some of the most fundamental questions in science:
What were the first objects in the Universe? How do galaxies form?
What is the nature of the dark matter and dark energy? Where does the
large-scale structure of the universe come from? What is the fate of
the Universe?</p>

<p>The ICC is based at Durham University and is part of the <a href="http://ogdencentre.dur.ac.uk" target="_blank">Ogden Centre	for Fundamental Physics</a>. We offer both PhD and Msc postgraduate studentships. Members of the ICC also belong to the <a href="http://astro.dur.ac.uk/cosmology/" target="_blank">Centre for Extragalactic Astronomy</a> in the <a href="http://www.dur.ac.uk/physics" target="_blank">Department of Physics</a>, and interact closely with staff in its observational section and the <a href="http://www.cfai.dur.ac.uk/new/" target="_blank">Centre for Advanced Instrumentation</a> at Durham University.</p>
