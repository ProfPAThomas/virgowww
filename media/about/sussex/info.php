<h1>Unversity of Sussex</h1>

<p>Website: <a href="http://www.sussex.ac.uk/">http://www.sussex.ac.uk</a>.</p>

<p>The University of Sussex is a leading higher education and research institution near Brighton, in the south of England. Sussex was the first of the new wave of UK universities founded in the 1960s, receiving its Royal Charter in 1961.</p>

<p>Sussex is a leading research university, as reflected in the 2014 Research Excellence Framework (REF). Over 75 per cent of research activity at Sussex is categorised as world leading (4*) or internationally excellent (3*) in terms of originality, significance and rigour.</p>
