<h1>What is the Virgo Consortium?</h1>

<p>The Virgo Consortium for Cosmological Supercomputer Simulations was
founded in 1994 in response to the UK's High Performance Computing
Initiative.  Virgo developed rapidly into the international
collaboration that it is today. The Virgo Consortium has a core
membership of 17 scientists in the UK, Germany, Netherlands, Canada,
USA and China.</p>

<p>The Virgo core members belong to the following universities and institutes:
UK - the universities of Durham, Edinburgh, Liverpool
John Moores, Manchester, Nottingham and Sussex;  Germany - the
Institutes: MPA,  Garching and HITS,  Heidelberg;  Netherlands -  Leiden
University;  USA: Johns Hopkins University;  Canada -  McMaster, Queen's
universities;  China: NAOC, Beijing.  At any given time, around 70 or so scientists, mostly PhD students and postdocs, are directly
involved in aspects of the Virgo programme.</p>

<p>The science goals of Virgo are to carry out state-of-the-art
cosmological simulations. The research areas include the large-scale
distribution of dark matter, the formation of dark matter haloes, the
formation and evolution of galaxies and clusters, the physics of the
intergalactic medium and the properties of the intracluster gas.</p>

<h1>Resources</h1>

<p>Virgo has access to world class supercomputing resources in the UK
and Germany.  In the UK Virgo makes extensive use of COSMA4 and the Data centric facility (known collectively as <a href="https://www.cosma.dur.ac.uk/HPC-help/home">COSMA</a>) at Durham, the latter of which is part of the <a href="http://www.dirac.ac.uk">DiRAC-2</a> facility. </p>

