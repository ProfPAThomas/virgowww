<p>From <a href="http://en.wikipedia.org/wiki/Leiden_Observatory">Wikipedia</a>:</p>

<p>Leiden Observatory is an astronomical observatory in the city of Leiden, the Netherlands. It was established by Leiden University in 1633, to house the quadrant of Rudolph Snellius, making it the oldest operating university observatory in the world, with the only older still existing observatory being the Vatican Observatory.</p>

<p>The observatory was originally located on the university building in the centre of Leiden before a new observatory building and dome were constructed in the university's botanical garden in 1860. It remained there until 1974 when the department moved to the science campus north-west of the city.</p>
