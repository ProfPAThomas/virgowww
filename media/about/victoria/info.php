<p>The University of Victoria offers innovative programs for more than 20,000 students. Dynamic, hands-on learning, research that makes a vital impact, and discovery and innovation in Canada’s most extraordinary academic environment provide an Edge that can’t be found anywhere else.</p>

<p>In early 2013, UVic was ranked number 20 globally and first in Canada among universities less than 50 years old by Times Higher Education (THE). It also ranks in THE's elite global list of the top 200 universities, which includes approximately one per cent of the world's best institutions.</p>

<p>Beyond the classroom, UVic is part of the community. With civic engagement a key part of our mission, our faculty, students and staff collaborate with a diverse range of community partners in Canada and abroad on essential issues that matter to people, places and the planet.</p>
